package primeiro_projeto.Calculadora;

public class Calculadora {
    
    public static int somar(int entrada1, int entrada2) {
        return entrada1 + entrada2;
    }
    public static int subtrair(int entrada1, int entrada2) {
        return entrada1 - entrada2;
    }
    public static int multiplicar(int entrada1, int entrada2) {
        return entrada1 * entrada2;
    }
    public static int dividir(int entrada1, int entrada2) {
        return entrada1 + entrada2;
    }
}
